<h2 id = "introduction">Introduction</h2>

<p>You are innocent until proven guilty. Based on this, you deserve privacy in your communications, not to be under surveillance constantly by governments and corporations.</p>

<p>This is a small project on cryptography I have developed just for fun. While I did my best, it might have some bugs. Please feel free to hunt them down and point them out, the code is Open Source and <a href = "http://github.com/FranciscoP/secretdiary">can be found on Github</a>.</p>

<p>For clarity on this page, a <strong>secret</strong> is the message to be hidden, a <strong>door</strong> is the entry point for it where we ask for your <strong>key</strong>, a password. A door has the following format: http://secretdiary.org/ID/ , where the ID is the file id which is stored in the database.</p>

<p><span style = "color: red;">IMPORTANT:</span> The site is not complete, we still do not provide https nor have a certificate. </p>

<h2 id = "howto">How does this page work?</h2>

<h3>Overview</h3>
<p>To <strong>store</strong> the text, we use the password to encrypt the message and save it in the database.
After storing the encrypted text and providing you with a link to it, the password is permanently deleted.</p>
<p>To <strong>retrieve</strong> the text, you enter the link provided and the password.
We retrieve the encrypted text for the link you provided and attempt to decrypt it.</p>

<h3>Detailed description of the process: Encryption.</h3>

<ol>
  <li>
    <p>You <strong>write</strong> and <strong>submit</strong> your text.</p>
  </li>
  <li>
    <p>The <strong>filename</strong> and <strong>password</strong> are autogenerated as hashes (sha1) of strings generated randomly (8 alphanumeric characters) if they are not set by you.</p>
  </li>
  <li>
    <p>The message is encrypted using <strong>MCRYPT_RIJNDAEL_256</strong>, <a href = "http://en.wikipedia.org/wiki/Advanced_Encryption_Standard">the most secure symmetric encryption algorythm</a>.
  </li>
  <li>
    <p>The encrypted text and filename are stored in the database while the password is not.</p>
  </li>
  <li>
    <p>The link for the message is shown but the password is only shown if this was autogenerated. When you navigate away from this page, the password is gone for good.</p>
  </li>
</ol>

