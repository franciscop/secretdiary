<h2 id = "faq">Frequently asked questions</h2>
<p><strong>Q: I forgot my password/key, can I recover it?</strong></p>
<p>A: We are sorry to hear that, but you <strong>cannot</strong> recover it. We simply don't have the means to do so.
If we did, we could read your messages.</p>

<p><strong>Q: Can anyone without the key but with the link to my secret message read it?</strong></p>
<p>A: <strong>No.</strong> All the data is kept encrypted and the key is required for decrypting it.
The data stored in the database is nothign but a bunch of random numbers with little to no value for anyone that doesn't hold the key used originally to encrypt it.</p>

<p><strong>Q: Will you hand over my encrypted message to the authorities if a search warrant is issued?</strong></p>
<p>A: <strong>Yes.</strong> We will abide by the law. This page does not support any illegal act, so we will both hand the encrypted data and delete it from the database if a court order is emited to do so.
However, this is useless data to anyone unless you chose a short, simple password or they have had access to it  (e.g. you stored it in your hard drive and they have also a search warrant for it).</p>

<p><strong>Q: Can YOU read my message?</strong></p>
<p>A: <strong>No.</strong> Read the precedent answer for more information.</p>

<p><strong>Q: Can I use <a href = "http://en.wikipedia.org/wiki/Plausible_deniability" target="_blank">plausible deniability</a> in this site somehow?</strong></p>
<p>A: Actually <strong>yes</strong>. See the <a href = "#howto">howto</a> for more information about how to accomplish this.</p>

